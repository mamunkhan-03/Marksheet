    <script>
        document.addEventListener("DOMContentLoaded", function() {
            const rows = document.querySelectorAll(".row");

            rows.forEach(row => {
                const inputFields = row.querySelectorAll(".input-field");
                inputFields.forEach(input => {
                    input.addEventListener("input", () => updateRowTotal(row));
                });
            });
        });

        function updateRowTotal(row) {
            const inputFields = row.querySelectorAll(".input-field");
            let rowTotal = 0;

            inputFields.forEach(input => {
                if (input.value !== "") {
                    rowTotal += parseFloat(input.value);
                }
            });


            var x = row.querySelector(".totall").textContent = rowTotal.toFixed(2);
            if (x > 70) {
                alert("Totall Marks Can't be greater than 70 ");
                row.querySelector(".totall").textContent = 0.00;
            }

        }

